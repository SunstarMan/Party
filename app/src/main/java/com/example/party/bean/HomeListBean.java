package com.example.party.bean;

/*
HomeFragment中新闻列表
 */
public class HomeListBean {
    private String homeListImage;
    private String homeListTitle;
    private String homeListTime;
    private String homeListBrowse;

    public String getHomeListImage() {
        return homeListImage;
    }

    public void setHomeListImage(String homeListImage) {
        this.homeListImage = homeListImage;
    }

    public String getHomeListTitle() {
        return homeListTitle;
    }

    public void setHomeListTitle(String homeListTitle) {
        this.homeListTitle = homeListTitle;
    }

    public String getHomeListTime() {
        return homeListTime;
    }

    public void setHomeListTime(String homeListTime) {
        this.homeListTime = homeListTime;
    }

    public String getHomeListBrowse() {
        return homeListBrowse;
    }

    public void setHomeListBrowse(String homeListBrowse) {
        this.homeListBrowse = homeListBrowse;
    }
}
